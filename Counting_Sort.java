class Counting_Sort {

    public static List<Integer> countingSort(List<Integer> arr) {
    List<Integer> arr1=new  ArrayList<Integer>(100);
    for(int i=0;i<=99;i++) {
        arr1.add(0);
        }
    for(int i=0;i<arr1.size();i++){
        arr1.set(arr.get(i),arr1.get(arr.get(i))+1);
        }
    
    return arr1;
    }

}

